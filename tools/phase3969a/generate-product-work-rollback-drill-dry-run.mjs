import { mkdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";
import { pathToFileURL } from "node:url";

const repoRoot = process.cwd();
const docPath = "docs/phase3969a-product-work-mode-rollback-drill-dry-run.md";
const resultPath = "apps/ai-gateway-service/evidence/phase3969a-product-work-mode-rollback-drill-dry-run/result.json";

function ensureParent(relativePath) {
  mkdirSync(resolve(repoRoot, relativePath, ".."), { recursive: true });
}

function writeJson(relativePath, value) {
  ensureParent(relativePath);
  writeFileSync(resolve(repoRoot, relativePath), `${JSON.stringify(value, null, 2)}\n`, "utf8");
}

function writeText(relativePath, value) {
  ensureParent(relativePath);
  writeFileSync(resolve(repoRoot, relativePath), value, "utf8");
}

export function generateProductWorkRollbackDrillDryRun() {
  const coveredPhases = [
    "Phase3959A owner daily use files",
    "Phase3960A ingest/classifier files",
    "Phase3961A scan files",
    "Phase3962A provider matrix files",
    "Phase3963A CredentialRef readiness files",
    "Phase3964A self evolution kernel files",
    "Phase3965A phase selection policy files",
    "Phase3966A low-risk self patch dry-run files",
    "Phase3967A provider smoke approval files",
    "Phase3968A dashboard files",
  ];
  const rollbackSteps = [
    "Delete tools/phase3959a through tools/phase3970a only after owner approval.",
    "Delete phase docs/evidence generated by Phase3959A-3970A.",
    "Keep owner-provided docs/owner-daily-use/records files unless owner explicitly asks deletion.",
    "Restore package.json scripts added by this AIO.",
    "Remove Product Work Mode dashboard import/render from MissionControlPanel.js.",
    "Restore README.md and AGENTS.md managed block entries through sync script or patch.",
  ];
  const result = {
    completed: true,
    recommendedSealed: true,
    blocker: null,
    rollbackDrillDryRunGenerated: true,
    realRollbackExecuted: false,
    filesDeleted: false,
    coveredPhases,
    rollbackSteps,
    providerCallsMade: false,
    secretRead: false,
    chatModified: false,
    chatGatewayExecuteModified: false,
    legacyModified: false,
    projectContextModified: false,
    deployExecuted: false,
    controlledMutationExpansionAttempted: false,
  };

  writeText(
    docPath,
    `# Phase3969A Product Work Mode Rollback Drill Dry-Run\n\n## Goal\n\nCreate a dry-run rollback plan for Product Work Mode files generated by Phase3959A-3968A. This does not execute rollback and does not delete files.\n\n## Covered Scope\n\n${coveredPhases.map((item) => `- ${item}`).join("\n")}\n\n## Dry-Run Rollback Steps\n\n${rollbackSteps.map((item) => `- ${item}`).join("\n")}\n\n## Boundary\n\n- realRollbackExecuted=false\n- filesDeleted=false\n- providerCallsMade=false\n- secretRead=false\n- deployExecuted=false\n\n## Rollback Of This Phase\n\n- Delete \`tools/phase3969a/\`.\n- Delete \`docs/phase3969a-product-work-mode-rollback-drill-dry-run.md\`.\n- Delete \`apps/ai-gateway-service/evidence/phase3969a-product-work-mode-rollback-drill-dry-run/\`.\n- Restore package.json scripts and README/AGENTS managed block entries.\n`,
  );
  writeJson(resultPath, result);
  return result;
}

export function main() {
  const result = generateProductWorkRollbackDrillDryRun();
  console.log(JSON.stringify({
    completed: result.completed,
    blocker: result.blocker,
    rollbackDrillDryRunGenerated: result.rollbackDrillDryRunGenerated,
    realRollbackExecuted: result.realRollbackExecuted,
    filesDeleted: result.filesDeleted,
  }, null, 2));
}

if (process.argv[1] && import.meta.url === pathToFileURL(process.argv[1]).href) {
  main();
}
