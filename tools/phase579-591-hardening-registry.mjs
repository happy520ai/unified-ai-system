function group(number, title, slug, phaseRows, options = {}) {
  const groupKey = `phase${number}`;
  const subphases = phaseRows.map((row, index) => {
    const [titleText, requiredFlag, rowOptions = {}] = row;
    const letter = letters[index];
    const key = `${groupKey}${letter}`;
    const phase = `Phase${number}${letter.toUpperCase()}`;
    const phaseSlug = slugify(titleText);
    const mergedOptions = {
      missionControlUi: options.missionControlUi === true || rowOptions.missionControlUi === true,
      workforceBranchFabric: options.workforceBranchFabric === true || rowOptions.workforceBranchFabric === true,
    };
    return {
      groupNumber: number,
      groupKey,
      groupTitle: title,
      groupSlug: slug,
      key,
      phase,
      letter,
      name: titleText,
      slug: phaseSlug,
      requiredFlag,
      goal: options.goal,
      docPath: `docs/${key}-${phaseSlug}.md`,
      reportPath: `docs/${key}-execution-report.md`,
      evidencePath: `apps/ai-gateway-service/evidence/${key}/${phaseSlug}-result.json`,
      verifierPath: `tools/${key}/validate-${key}-${phaseSlug}.mjs`,
      packageScript: `verify:${key}-${phaseSlug}`,
      missionControlUi: mergedOptions.missionControlUi,
      workforceBranchFabric: mergedOptions.workforceBranchFabric,
    };
  });
  return {
    number,
    key: groupKey,
    title,
    slug,
    goal: options.goal,
    subphases,
    sequenceEvidencePath: `apps/ai-gateway-service/evidence/${groupKey}a-t-auto-verified-sequential-execution.json`,
    packageScript: `verify:${groupKey}a-t-${slug}`,
  };
}

function slugify(value) {
  return value
    .replace(/\/|\+/g, " ")
    .replace(/P0|P1|P2|P3/g, (match) => match.toLowerCase())
    .replace(/WeCom/g, "wecom")
    .replace(/Codex/g, "codex")
    .replace(/E2E/g, "e2e")
    .replace(/IO/g, "io")
    .replace(/UI/g, "ui")
    .replace(/UX/g, "ux")
    .replace(/API/g, "api")
    .replace(/README AGENTS/g, "readme-agents")
    .replace(/[^A-Za-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "")
    .toLowerCase();
}
